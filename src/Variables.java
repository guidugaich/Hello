public class Variables {
    // integer data types:
    // byte - 8 bits
    // short - 16 bits
    // int - 32 bits
    // long - 64 bits

    // floating data types
    // float - 32 bits = 4.5f (needs the f)
    // double (default for decimal values) - 64 bits

    // char - 16 bits (single character)

    // boolean - true/false

    public static void main(String[] args) {
        int a = 234234;
        System.out.println(a);

        char letra = 'w';
        System.out.println(letra);

        boolean verdade = true;
        System.out.println(verdade);

        System.out.println(1 + 2); // prints 3
        System.out.println("1" + 2); // prints 12

        // if there is only one string, it will CONCATENATE
    }
}
